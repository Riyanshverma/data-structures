#include<stdio.h>
void bubbleSort(int arr[],int n){
    for(int i=0;i<n-1;i++){
        int flag=1;
        for(int j=0;j<n-i-1;j++){
            if(arr[j]>arr[j+1]){
                flag=0;
                int temp=arr[j];
                arr[j]=arr[j+1];
                arr[j+1]=temp;
            }
        }
        if(flag){
            break;
        }
    }
}